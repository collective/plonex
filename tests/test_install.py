    def _fake_requirement(
        self,
        name="pkg",
        marker=None,
        path="/tmp/pkg",
        filename="pkg",
        editable=False,
        dumps=None,
    ):
        return SimpleNamespace(
            name=name,
            marker=marker,
            is_editable=editable,
            link=SimpleNamespace(path=path, filename=filename),
            dumps=(lambda: dumps if dumps is not None else name),
        )

