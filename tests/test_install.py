    def test_collect_constraints_remote_include_can_be_overridden_locally(self):
        with temp_cwd():
            install = InstallService(dont_ask=True)

            (install.constraints_d_folder / "remote.txt").write_text(
                "-c https://example.org/constraints.txt\n" "bar==1.0.0\n"
            )

            remote_response = mock.Mock()
            remote_response.text = "bar==2.0.0\nfoo==3.0.0\n"
            remote_response.raise_for_status = mock.Mock()

            def fake_get(url, timeout):
                if url == "https://example.org/constraints.txt":
                    return remote_response

                default_response = mock.Mock()
                default_response.text = ""
                default_response.raise_for_status = mock.Mock()
                return default_response

            with mock_remote_constraints(
                {"https://example.org/constraints.txt": "bar==2.0.0\nfoo==3.0.0\n"}
            ):
                with install:
                    self.assertListEqual(
                        install.constrainst_txt.read_text().splitlines(),
                        [
                            "# This file is generated by plonex",
                            "bar==1.0.0",
                            "foo==3.0.0",
                        ],
                    )

                    _, merged_constraints = install._collect_constraint_entries(
                        install.constrainst_txt
                    )

            resolved = {
                install._requirement_key(requirement): requirement.dumps()
                for requirement in merged_constraints.values()
            }
            self.assertEqual(resolved["bar"], "bar==1.0.0")
            self.assertEqual(resolved["foo"], "foo==3.0.0")

