@contextmanager
def temp_install(**kwargs):
    with temp_cwd():
        kwargs["dont_ask"] = True
        with InstallService(**kwargs) as client:
            yield client


@contextmanager
def mock_remote_constraints(mapping=None):
    mapping = mapping or {}

    def fake_get(url, timeout):
        response = mock.Mock()
        response.text = mapping.get(url, "")
        response.raise_for_status = mock.Mock()
        return response

    with mock.patch("plonex.install.requests.get", side_effect=fake_get):
        yield


